#include <stdio.h>
#include <locale.h>
#include <math.h>  // Для использования функции sin()

int main()
{
    double n;      // Введённое пользователем число
    double k = 0;  // Сумма гармонического ряда (накапливаемая сумма 1/i)
    int i = 1;     // Счётчик, текущий знаменатель дроби 1/i
    int mass[20];  // Массив для хранения значений синуса

    setlocale(LC_ALL, "Russian");
    printf("Введите число n: ");
    scanf_s("%lf", &n);

    // Проверка: если n меньше или равно 0, то число не является положительным
    if (n <= 0)
    {
        printf("Ошибка: число n должно быть положительным!\n");
        return 1;  // Завершение программы с кодом ошибки
    }

    // Заполнение массива значениями синуса от номера итерации
    for (int j = 0; j < 20; j++) {
        mass[j] = (int)(sin(j) * 1000); // Пример: масштабируем синус для наглядности, можно убрать преобразование в int
    }

    // Выводим массив для проверки (опционально)
    printf("Массив mass:\n");
    for (int j = 0; j < 20; j++) {
        printf("mass[%d] = %d\n", j, mass[j]);
    }

    // Ваш оригинальный цикл для вычисления суммы гармонического ряда
    while (k < n)
    {
        k = k + 1.0 / i;
        i++;
    }

    printf("Наименьшее число, большее числа %lf: %lf\n", n, k);
    return 0;
}