#include <stdio.h>
#include <locale.h>
#include <math.h>  // Для использования функции sin()

int main()
{
    double n;      // Введённое пользователем число
    double k = 0;  // Сумма гармонического ряда
    int i = 1;     // Счётчик для знаменателя

    setlocale(LC_ALL, "Russian");

    printf("Введите число n: ");
    scanf_s("%lf", &n);

    // Проверка на положительность числа
    if (n <= 0)
    {
        printf("Ошибка: число n должно быть положительным!\n");
        return 1;
    }

    // Массив для хранения значений синуса
    int mass[20];

    // Заполняем массив значениями синуса (масштабируем для наглядности)
    for (int j = 0; j < 20; j++) {
        mass[j] = (int)(sin(j) * 1000);
    }

    // Красивый вывод массива в виде таблицы
    printf("\n--- Содержимое массива mass (масштаб *1000) ---\n");
    printf(" Индекс | Значение\n");
    printf("--------|----------\n");

    for (int j = 0; j < 20; j++) {
        printf(" mass[%2d] | %7d\n", j, mass[j]);
    }

    printf("\n");

    // Вычисление суммы гармонического ряда, пока k не станет >= n
    while (k < n)
    {
        k += 1.0 / i;
        i++;
    }

    printf("Наименьшее число, большее числа %lf: %lf\n", n, k);

    return 0;
}